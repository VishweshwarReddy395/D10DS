
# Complete example of Conditional expressions

# if_elif_else ladder

marks = int(input("Enter your marks : "))

if marks==90:
    print("You have Scored O Grade")

elif marks==70:
    print("You have Scored A+ Grade")

elif marks==60:
    print("You have Scored B Grade")

elif marks==40:
    print("You have Scored C Grade")

elif marks==30:
    print("You have Scored D Grade")

else:
    print("You have failed the Exam")


print("Congrats and Better Luck Next Time")